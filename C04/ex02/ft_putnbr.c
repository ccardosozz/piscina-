#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	long	n;

	n = nb;
	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
	}
	if (n >= 10)
	{
		ft_putnbr (n / 10);
	}
	ft_putchar(n % 10 + '0');
}

int	main(void)
{
	ft_putnbr(-123231);
}